#define GPIO_REG_FALL_INT_PENDING 0x24
#define GPIO_REG_HIGH_INT_EN 0x28
#define GPIO_REG_HIGH_INT_PENDING 0x2C
#define GPIO_REG_LOW_INT_EN 0x30
#define GPIO_REG_LOW_INT_PENDING 0x34
#define GPIO_REG_IOF_EN 0x38
#define GPIO_REG_IOF_SEL 0x3C
